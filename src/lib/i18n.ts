const bundles: Record<SupportedAppLanguage, TranslationBundle> = {
  en,
  he,
  ru,
};

export const APP_LANGUAGE_META: Record<SupportedAppLanguage, AppLanguageMeta> = {
  en: { code: 'en', label: 'English', nativeLabel: 'English', dir: 'ltr', locale: 'en' },
  he: { code: 'he', label: 'Hebrew', nativeLabel: 'עברית', dir: 'rtl', locale: 'he' },
  ru: { code: 'ru', label: 'Russian', nativeLabel: 'Русский', dir: 'ltr', locale: 'ru' },
};

let runtimeAppLanguage: SupportedAppLanguage = 'en';

export function resolveSupportedAppLanguage(value?: string | null): SupportedAppLanguage {
  const normalized = value?.trim().toLowerCase();

  if (!normalized) {
    return 'en';
  }

  if (normalized === 'he' || normalized.startsWith('he-') || normalized.startsWith('iw')) {
    return 'he';
  }

  if (normalized === 'ru' || normalized.startsWith('ru-')) {
    return 'ru';
  }

  return 'en';
}

export function resolveInitialAppLanguage(): SupportedAppLanguage {
  if (typeof navigator === 'undefined') {
    return 'en';
  }

  return resolveSupportedAppLanguage(navigator.languages?.[0] ?? navigator.language);
}

export function setRuntimeAppLanguage(language: SupportedAppLanguage): void {
  runtimeAppLanguage = language;
}

export function getRuntimeAppLanguage(): SupportedAppLanguage {
  return runtimeAppLanguage;
}

export function getRuntimeLanguageMeta(): AppLanguageMeta {
  return APP_LANGUAGE_META[runtimeAppLanguage];
}

export function getLanguageMeta(language: SupportedAppLanguage): AppLanguageMeta {
  return APP_LANGUAGE_META[language];
}

export function getRuntimeIntlLocale(): string {
  return getRuntimeLanguageMeta().locale;
}

function readMessage<K extends TranslationKey>(
  bundle: TranslationBundle,
  key: K,
): TranslationBundle[K] {
  return bundle[key];
}

export function translate<K extends TranslationKey>(
  language: SupportedAppLanguage,
  key: K,
  params?: Record<string, string | number | undefined>,
): string {
  const bundle = bundles[language];
  const entry = readMessage(bundle, key);

  if (typeof entry === 'function') {
    return entry((params ?? {}) as never);
  }

  return entry;
}

export function createTranslator(language: SupportedAppLanguage) {
  return <K extends TranslationKey>(
    key: K,
    params?: Record<string, string | number | undefined>,
  ) => translate(language, key, params);
}

export function tRuntime<K extends TranslationKey>(
  key: K,
  params?: Record<string, string | number | undefined>,
): string {
  return translate(runtimeAppLanguage, key, params);
}
